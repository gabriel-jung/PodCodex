def polish_segments(
    segments: list[dict],
    mode: str = "ollama",
    context: str = "",
    source_lang: str = "French",
    model: str = "",
    api_base_url: str = "",
    api_key: str | None = None,
    batch_minutes: float = DEFAULT_BATCH_MINUTES,
    original_segments: list[dict] | None = None,
    merge: bool = True,
    max_gap: float = DEFAULT_MAX_GAP,
    provider: str | None = None,
    engine: str = "Whisper",
    on_batch: Callable[[int, int], None] | None = None,
) -> list[dict]:
    """
    Correct transcription errors in segments.

    Args:
        segments          : transcript segments (for ollama/api mode),
                            or LLM output with corrections (for manual mode)
        mode              : "manual", "ollama", or "api"
        context           : podcast context to guide the LLM
        source_lang       : source language (e.g. "French")
        model             : LLM model name (auto-detected from provider if empty)
        api_base_url      : base URL for OpenAI-compatible API (auto-detected from provider if empty)
        api_key           : API key (None reads from provider's env variable)
        batch_minutes     : maximum audio duration per LLM batch in minutes (default 15)
        merge             : merge consecutive same-speaker segments before processing (default True)
        max_gap           : maximum silence gap in seconds for merging (default 10.0)
        engine            : transcription engine name for the prompt (default "Whisper")
        provider          : provider shorthand ("openai", "anthropic", "mistral", "groq")
                            — sets api_base_url, model, and api_key env var automatically
        original_segments : only for manual mode — the original source segments
                            (used to merge metadata with the LLM-provided corrections)

    Returns:
        List of segments with corrected text field.
    """
    logger.info(f"Polishing {len(segments)} segments — mode={mode}")
    logger.debug(
        f"Polish params: lang={source_lang}, engine={engine}, batch_minutes={batch_minutes}, merge={merge}"
    )
    if context:
        logger.debug(f"Context: {context[:100]}{'…' if len(context) > 100 else ''}")

    if mode == "manual":
        orig = original_segments if original_segments is not None else segments
        return validate_manual(segments, orig)

    if merge:
        segments = merge_consecutive_segments(segments, max_gap=max_gap)
        logger.info(f"After merge: {len(segments)} segments")

    system_prompt = _build_prompt(context, source_lang=source_lang, engine=engine)

    if mode == "ollama":
        result = run_ollama(
            segments,
            system_prompt,
            model=model or "qwen3:4b",
            batch_minutes=batch_minutes,
            instruction="Correct",
            label="Polish",
            on_batch=on_batch,
        )
    elif mode == "api":
        result = run_api(
            segments,
            system_prompt,
            model=model,
            api_base_url=api_base_url,
            api_key=api_key,
            batch_minutes=batch_minutes,
            provider=provider,
            instruction="Correct",
            label="Polish",
            on_batch=on_batch,
        )
    else:
        raise ValueError(
            f"Unknown mode: {mode!r}. Choose from 'manual', 'ollama', 'api'."
        )

    logger.success(f"Polish done — {len(result)} segments")
    return result


